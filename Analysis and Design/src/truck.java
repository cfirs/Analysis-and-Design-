
public class truck {
	
	public int id;
	public String model;
	public String color;
	public double net_weight;
	public double max_weight;
	
	
	
	public truck(int id, String model, String color, double net_weight, double max_weight)
	{
		this.id = id;
		this.model = model;
		this.color = color;
		this.net_weight = net_weight;
		this.max_weight = max_weight;
	}
	
	
	
	

}
